package practice_kotlin

fun main() {
//------------------------------------------------------------------------
    for (i in 1..10) print("$i ")
    println("     <- (1 to 10) print")
//------------------------------------------------------------------------
    for (i in 1..<10) print("$i ")
    println("        <- (1 to 10-1) print")
//------------------------------------------------------------------------
    for (i in 10 downTo 1) print("$i ")
    println("     <- (Reverse ) print")
//------------------------------------------------------------------------
    for (i in 3..30 step 3) print("$i ")
    println()
//------------------------------------------------------------------------
    for (i in 9..90 step 9) print("$i ")
    println()
    println()
//------------------------------------------------------------------------
    val num1 = 3
    for (i in 1..10) println("$num1 X $i = ${i * num1}")
    println()
//------------------------------------------------------------------------
    val num2 = 9
    for (i in 1..10) println("$num2 x $i = ${num2 * i}")
    println()
//------------------------------------------------------------------------
    val language: Array<String> = arrayOf(
        "C",
        "C++",
        "Java",
        "Kotlin",
        "Html",
        "Css",
        "Java Script",
        "Python",
        "Swift"
    )
//------------------------------------------------------------------------
    print("| ")
    for (i in 0..<language.size) print("${language[i]} | ")
//------------------------------------------------------------------------
    print("\n| ")
    for (i in language) print("$i | ")
//------------------------------------------------------------------------
    print("\n| ")
    for (i in language.size - 1 downTo 0) print("${language[i]} | ")
//------------------------------------------------------------------------
}